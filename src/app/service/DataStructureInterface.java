package app.service;

/**
 * Interface for demonstrating Data Structure operations.
 * Each implementing class (e.g., DS_ArrayList) will provide specific demos for that DS.
 */
public interface DataStructureInterface {
    /**
     * Demonstrates basic operations like add, remove, size, and simple iteration/printing.
     */
    void demoBasicOperations();

    /**
     * Demonstrates search operations like contains, indexOf, etc.
     */
    void demoSearchOperations();

    /**
     * Demonstrates sorting operations, including normal and custom sorts if applicable.
     */
    void demoSortOperations();

    /**
     * Demonstrates advanced features like utilities, iterators, capacity management, etc., if applicable.
     */
    void demoAdvanced();
}
